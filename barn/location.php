<h2>Getting to Reddivallen by car:</h2>
<p>
  Reddivallen is approximately 75 minutes drive  from Exeter.<br />
  From Exeter on the A30 to Launceston take the A395/A39 to Camelford. <br />
  Before Camelford turn right on to the B3314 for approximately 2 miles until you come to some crossroads (cycling museum on the right). Turn right on to the B3266 to Boscastle. Travel through another crossroads for 500 yards and Reddivallen is sign-posted on the left. Our entrance has cornish stone walling with a slate sign and Reddivallen is down the tarmac lane.
</p>
<h2>Alternatively:</h2>
Reddivallen is -
<ul>
  <li>90 minutes drive from Exeter Railway Station or Airport;</li>
  <li>30 minutes from Bodmin Parkway Station;</li>
  <li>45 minutes from Newquay Airport &ndash; Car Hires available</li>
</ul> 

There is also a Western Greyhound bus route between Bude and  Wadebridge with links via Camelford to Exeter. <br />
